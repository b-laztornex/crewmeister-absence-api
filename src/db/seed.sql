DROP TABLE IF EXISTS members;
CREATE TABLE members (
    id SERIAL PRIMARY KEY,
    crewId INTEGER,
    userId INTEGER,
    name VARCHAR(255),
    image VARCHAR(255)
);

DROP TABLE IF EXISTS absences;
CREATE TABLE absences (
    id SERIAL PRIMARY KEY,
    crewId INTEGER,
    userId INTEGER,
    type VARCHAR(50),
    startDate DATE,
    endDate DATE,
    memberNote TEXT,
    admitterNote TEXT,
    confirmedAt TIMESTAMP,
    rejectedAt TIMESTAMP,
    createdAt TIMESTAMP
);

